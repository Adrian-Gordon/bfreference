from http.server import BaseHTTPRequestHandler, HTTPServer
from seq2seq import BfInferer
import json


class InferenceServerHandler(BaseHTTPRequestHandler):
  def _set_headers(self):
    self.send_response(200)
    self.send_header('Content-type','application/json')
    self.end_headers()

  def do_POST(self):
    if(self.path == '/predict'):
      self.doInference()

  def doInference(self):
    self._set_headers()

    self.data_string = self.rfile.read(int(self.headers['Content-Length'])).decode("utf-8")

    #print(self.data_string)
    self.data = json.loads(self.data_string)

    lay_predictions, back_predictions = bfInferer.doInference(self.data)
    predictions = {"lay":lay_predictions.tolist(),"back":back_predictions.tolist()}
    #print(lay_predictions)
    #print(back_predictions)
    self.wfile.write(json.dumps(predictions).encode('utf-8'))

bfInferer = BfInferer("./configs/config_bf.json")
      


if __name__ == '__main__':
  server = HTTPServer(('',3002), InferenceServerHandler)
  print("listening on port 3002")
  server.serve_forever()



